            <h3>A record is created using a form.</h3>
            <p><span class="bold">A Form is a group of customizable fields that you use to create metadata for your objects.</span>  When you create a new record, you fill out a form.  A project can have multiple forms depending on the types of objects you're storing or the audiences you're targeting.  You might have, for instance, one form for photographs and another form for essays that contextualize those photograph objects.</p>
